    private void bLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bLoginActionPerformed
        // TODO add your handling code here:
        try
        {
            Connection con=getDBconnection.getConnection();
            Statement stm=con.createStatement();
            String typedpass=String.valueOf(txtPassword.getPassword());
            String typedacc=txtAccount.getText();
            ResultSet record=stm.executeQuery("select pass from accounts where acc='"+typedacc+"'");
            if(record.next())
            {
                if(typedpass.equals(record.getString("pass")))
                {
                    JOptionPane.showMessageDialog(this,"Login successfully !");
                    svform=new qlsvForm();
                    svform.setVisible(true);
                    this.setVisible(false);
                }
                else
                    JOptionPane.showMessageDialog(this,"Wrong password !");
            }
            else
                JOptionPane.showMessageDialog(this,"Invalid username !");
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(this,e.getMessage());
        }
        
    }//GEN-LAST:event_bLoginActionPerformed

